  private void cboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTipoActionPerformed
    cboPublica.removeAllItems();
    Integer index = cboTipo.getSelectedIndex();
    if( index == -1 ){
      return;
    }
    Combo tipo = (Combo) cboTipo.getSelectedItem();
    List<Combo> lista;
    lista = finalController.leerPublicaciones(tipo.getCode());
    fillCombo(cboPublica, lista);
  }//GEN-LAST:event_cboTipoActionPerformed

  private void cboPublicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboPublicaActionPerformed
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblRepo.getModel();
    tabla.setRowCount(0);
    
    Integer index = cboPublica.getSelectedIndex();
    if( index == -1){
      return;
    }
    
    Combo combo = (Combo) cboPublica.getSelectedItem();
    List<Map<String,Object>> ventas;
    ventas = finalController.leerVentas(combo.getCode());
    
    for (Map<String, Object> venta : ventas) {
      Object[] rowData = {
        venta.get("idventa"),
        venta.get("cliente"),
        venta.get("total")
      };
      tabla.addRow(rowData);
    }
    
  }//GEN-LAST:event_cboPublicaActionPerformed


