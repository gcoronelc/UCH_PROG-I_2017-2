  private void fillCombo(JComboBox<Combo> control, 
          List<Combo> lista) {
    
    control.removeAllItems();
    for (Combo combo : lista) {
      control.addItem(combo);
    }
    
    control.setSelectedIndex(-1);
    
  }  
  

