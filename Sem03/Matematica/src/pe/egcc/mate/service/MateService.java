package pe.egcc.mate.service;

public class MateService {

    public int promediar(int n1, int n2){
        int pr;
        pr = (n1 + n2) / 2;
        return pr;
    }
    
    public int promediar(int n1, int n2, int n3){
        int pr;
        pr = (n1 + n2 + n3) / 3;
        return pr;
    }
        
}
